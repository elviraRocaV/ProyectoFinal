function cambiarFondoUsuarios(nombre)
{
    if(nombre.value.match(/^[A-Z][a-z]{1,14}$/))
    {
        nombre.style.backgroundImage= 'url("../imagenes/vistoVerde.png")';
    }else
    {
        nombre.style="border: 1px solid red;"
    }
}

